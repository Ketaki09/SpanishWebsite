           <h3> ¿Cuáles son los fines de LEAN? </h3>
LEAN está dedicada a fomentar valores e instaurarlos como principios, 
trabajar en formación cívica, promover y defender las libertades individuales y los derechos humanos en Venezuela,
 sensibilizar a la gente sobre la importancia de conocer, respetar y practicar los principios contenidos en la Declaración 
 Universal de Derechos Humanos, asistir a víctimas de actos violentos y persecución, favorecer la adquisición de conocimiento
  a través de la lectura y trabajar incansablemente en la ayuda humanitaria como gesto de solidaridad y buena voluntad.

