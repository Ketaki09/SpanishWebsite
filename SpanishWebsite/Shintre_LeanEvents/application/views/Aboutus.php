           <h3> ¿QUÉ HACEMOS? </h3>
        La asociación civil LEAN fue creada con el objetivo de ayudar,
        a través de acciones concretas, a nuestros conciudadanos en Venezuela ante la grave escasez de medicinas 
        e insumos médicos en que se encuentra el país. Nuestra misión consiste en recolectar ayuda médico sanitaria en 
        delegaciones en España y, a través de agentes de transporte, llevarlos a Venezuela para que otras asociaciones 
        se encarguen de su distribución. De esta manera aportamos nuestro granito de arena ayudando a llevar asistencia
        humanitaria a Venezuela. Somos una asociación sin fines de lucro, dedicada a la defensa de los Derechos Humanos 
