            <h3>¿Cuáles son las actividades de LEAN? </h3>
LEAN trabaja en el desarrollo y publicación de estudios de investigación 
sobre temas de interés social, cultural, político y económico, preparación de
 ponencias para foros y conferencias, presentación en eventos especializados y mesas
  de trabajo, petición de colaboración a personalidades de reconocida trayectoria, estudio de 
  casos de violación de derechos humanos a través de letrados voluntarios, asistencia y representación 
  para la defensa de las víctimas de actos violentos y persecución, lanzamiento de campañas sobre valores 
  cívicos y derechos humanos, planificación y ejecución de programas de voluntariado para brindar ayuda 
  humanitaria y organización de charlas sobre la situación económica, política y social de Venezuela y demás 
  temas de interés mundial.

